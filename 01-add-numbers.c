// This C Program adds two numbers

#include <stdio.h>
#include <stdlib.h>

int main() {
	int firstNumber = 23;
	int secondNumber = 7;
	
	// Add the two numbers
	int sumTotal = firstNumber + secondNumber;
	
	// Display the sum
	printf("The sum of %d and %d is %d.", firstNumber, secondNumber, sumTotal);
}

// OUTPUT
// The sum of 23 and 7 is 30.
